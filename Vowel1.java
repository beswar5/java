class  Vowel1
{
	public static void main(String[] args) 
	{
		char ch='+';
		if(ch>='a' && ch<='z')
		{
			System.out.println(ch+" is a lower case");
		}
		else if(ch>='A' && ch<='Z')
		{
			System.out.println(ch+" is  upper case");
		}
		else
		{
			System.out.println(ch+" is a not alphabet");
		}

	}
}
